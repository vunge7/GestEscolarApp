    public void entrar_sistema() throws SQLException {

        try {

            if (getCodico_Utilizador() != 0) {

                //dispose();
                new PrincipalVisao(getCodico_Utilizador()).show();
                limpar();

            } else if (getCodico_Professor() != 0) {
                //dispose();
                new AreaDocenteVisao(getCodico_Professor()).show();
                limpar();

            } else {
                JOptionPane.showMessageDialog(null, "ERRO: VERIFIQUE BEM A SENHA OU USER NAME", "SUPERLC, COMERCIAL", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "ERRO: VERIFIQUE BEM A SENHA OU USER NAME", "SUPERLC", JOptionPane.ERROR_MESSAGE);
        }

    }

    public int getCodico_Utilizador() {
        return usuarioDao.getUsuario_by_user_senha(getUserName(), getSenha()) != null ? usuarioDao.getUsuario_by_user_senha(getUserName(), getSenha()).getPkUsuario() : 0;
    }

    public int getCodico_Professor() {
        return professorDao.getProfessor_by_user_senha(getUserName(), getSenha()).getPkProfessor();
    }

    public String getUserName() {
        return txtUserName.getText();
    }

    public String getSenha() {
        return pswSenha.getText();
    }

    private void limpar() {

        txtUserName.setText("");
        pswSenha.setText("");

    }

